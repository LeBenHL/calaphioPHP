My name is Robert Yu and I want to be your next Fellowship Vice President. In my opinion, the best aspect of APO is fellowships. Many brothers say they joined APO for the service, but stayed for the friends. Without fellowships, these amazing friendships would not be possible. 
<br><br>
If elected, I would make multiple improvements to the fellowship program of our chapter. One major problem I see is the fact that many brothers only know a select number of people within the chapter. In order to address this problem, I would push for an increase in the amount of fellowships required within one semester. In my opinion, five fellowships is an extremely small amount. The point of having the fellowship program is to provide the brothers of the fraternity with opportunities to interact with each other in an informal setting. If brothers only do five fellowships, there will continue to be a sense of unfamiliarity between them. Instead, I would try to make the minimum requirement eight. By having eight fellowships, brothers would go out to more events and form stronger bonds.
<br><br>
Secondly, I want to address the actual logistics of fellowships. In the past two semesters, I feel like chairs have not been doing their job of making sure everyone is well engaged in the fellowship. I want the chapter to know that chairing a fellowship is more than just making sure everyone sings toast song. The chairs should be actively engaging everyone in the fellowship and trying to make sure that the participants actually intermix with each other as opposed to staying with one group. One way I can enforce this is by &quot;dropping in&quot; on more fellowships.
<br><br>
Also, I want to continue the push of making fellowships at the beginning of the semester. I believe GG Barbeque Retreat was an excellent way of starting off the semester. It gave people a chance to reconnect with brothers and make them excited about the semester. These events are a perfect way to transition from summer break back to school and APO. 
<br><br>
Moreover, I would like to create more GG events. This semester, GG Barbeque Retreat was a great success and GG Sky High is on track to be successful as well. These events create a good way for brothers to bond with the entire chapter as opposed to staying limited to their small families. One event that I would like to bring back is GG Olympics. In the past, this event was successful and I believe that it would allow brothers to bond over friendly competition. 
<br><br>
Overall, I believe these are the main aspects that need to be addressed. If elected FVP, I would do my absolute best to act in ways that will only benefit the chapter.  Thanks!
<br><br>

iLFS,
<br><br>
Robert Yu</p>

