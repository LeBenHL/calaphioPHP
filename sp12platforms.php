	 In all honesty, I cannot give a specific number or size at this moment; it all depends on future factors.  I am most likely going to keep the pledge class around CPZ size or smaller, but I want to take into account the number of bigs/aunts/uncles, to keep a one-to-one ratio between pledges and family members.     
