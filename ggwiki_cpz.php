<h1>Charles P. Zlatkovich (Fall 2011)</h1>
<hr />
<a href="ggwiki_ks.php">Katherine Strausser (KS)</a> | <a href="ggwiki_js.php">Jennifer Sun (JS)</a>
<br><br>

<img src="ggwiki_photos/cpz/cpz.jpg" align="left" Hspace="10">
Brother Zlatkovich served Alpha Phi Omega in a variety of roles since his initiation, including chapter delegate to the 1967 Constitutional Convention, Region VII Director (1970-1974), 
National Finance Chair (1974-1978), National Vice President (1978-1980), National President (1980-1982), and trustee on the Alpha Phi Omega Endowment Board of Trustees (1982-1994). 
Professionally, Brother Zlatkovich spent his career in higher education in Texas and retired as Associate Dean of the College of Business at the University of Texas at El Paso in 2002.<br><br>

In addition to his impressive resume of achievement in Alpha Phi Omega, what set Brother Zlatkovich apart was what he accomplished and the profound impact he had on so many other brothers 
through his service, said APO Vice President and Chair of the Fall Pledge Class Namesake Nomination Committee, John Ottenad, Alpha Phi  '89. His commitment to diversity in the Fraternity, his 
diligent, creative and untiring efforts to help lead the Fraternity out of difficult financial times, and his willingness to travel far and wide, mostly by car, on behalf of the Fraternity 
inspired a whole generation of leaders who have shaped our Fraternity for over 30 years.<br><br>

(Source: http://www.apo.org)
