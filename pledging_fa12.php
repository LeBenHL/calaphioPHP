	<h2><a name="faq">Frequently Asked Questions</a></h2>
	<br> <br/> <!-- for page formatting -->
	<a name="q1"></a>
	<p style="font-weight: bold">What is Alpha Phi Omega?</p>
	<p>Alpha Phi Omega is an international co-ed service fraternity with over 80 years of college volunteerism; it is the largest fraternity in the world. Our chapter at UC Berkeley consists of over 130 active members who contribute 10,000 hours of service annually. In addition to service we have numerous events during the semester to promote friendship and brotherhood within the chapter and with chapters on other campuses.</p>
	<a name="q2"></a>
	<p style="font-weight: bold">Do I have to attend Rush in order to pledge APO?</p>
	<p>No.  Our Rush events are for you to find out more about what APO is all about.  They are meant to provide a glimpse into both the service we do, and meet the people in the fraternity.</p>
	<a name="q3"></a>
	<p style="font-weight: bold">Do I have to attend Ritual in order to pledge APO?</p>
	<p>Yes, it is imperative to attend Ritual. To find out more information, please attend our Rush events to find out more. Please dress in semi formal attire.</p>
	<a name="q3half"></a>
	<p style="font-weight: bold">Is there a cap to the pledge class this semester?</p>
	<p>Yes, this semester we will be limiting our pledge class. This will be on a first-serve basis, not with any screenings or interviews in order to be as fair as possible. More details will be given during Rush events.</p>
	<a name="q4"></a>
	<p style="font-weight: bold">What's pledging like?</p>
	<p>Your first semester in APO will be full of opportunities to learn about the organization and find your place in it. That is the essence of the pledge process. You will undergo an initiation ritual, and be assigned to a "big" -- an older APO member who will answer any questions you have and help you figure out the ins and outs of APO. You will attend all the same meetings as the Active Brothers do, and you will also attend pledge meetings, which help you get to know other people as well as the history and purpose of APO.</p>
	<a name="q5"></a>
	<p style="font-weight: bold">Does APO Haze?</p>
	<p>No.  We are a community service fraternity. Members of this fraternity will never haze you in any way, shape or form, nor will they demand any form of personal service from you.</p>
	<a name="q6"></a>
	<p style="font-weight: bold">Is Alpha Phi Omega a Greek fraternity?</p>
	<p>No.  We are a fraternity in the aspect of brotherhood and camaraderie.  Although we use a Greek name, and have many of our own rituals and traditions, we are not affiliated with IFC or Panhel.</p>
	<a name="q7"></a>
	<p style="font-weight: bold">I'm already in another fraternity or sorority, Can I still pledge APO?</p>
	<p>Yes.  APO is open to any student on campus.  Joining APO will not conflict with your ability to join or remain a member of any other fraternity or sorority.</p>
	<a name="q8"></a>
	<p style="font-weight: bold">What are the pledge requirements?</p>
	<p>Pledge Requirements include weekly attendance at meetings, 20 hours of community service, fellowship/social gatherings, and service on a committee.  Most meetings will be on Tuesday evenings, as such, if you have other commitments it will be extremely difficult to pledge this semester. More details will be given at our Rush events.</p>
	<a name="q9"></a>
	<p style="font-weight: bold">What kind of fellowships/social events do the brothers of APO put on?</p>
	<p>Alpha Phi Omega hosts a wide variety of fellowships within our chapter and at other chapters in California. These events range from TV series events, dinners, potlucks, sports events, poker tournaments and more. To get a better idea of the types of events we host, check the calendar on our website.</p>
	<a name="q10"></a>
	<p style="font-weight: bold">What kind of service projects does APO participate in?</p>
	<p>Our chapter serves a wide range of organizations within the Bay Area region. We support the Adventure Playground, <a href="http://openhand.org">Project Open Hand, </a> <a href="http://sffoodbank.org">The San Francisco Food Bank</a>, Brewer Middle School, Cal Star Yoga and a number of other projects. To get a more complete idea, check the calendar. New projects are introduced each semester and our chapter organizes a large event each semester. In the fall it has traditionally been a Halloween Carnival at Emerson Elementary, and in the Spring it has been Eggstravanganza in Oakland.</p>
	<a name="q11"></a>
	<p style="font-weight: bold">What kind of leadership development does APO provide?</p>
	<p>Our fraternity participates in the national APO LEADS Program.  This is a national program designed to train interested members in leadership and team building skills, and is quickly becoming a popular and important aspect of Alpha Phi Omega. Several of these workshops are hosted at chapters in the area throughout the semester.</p>

