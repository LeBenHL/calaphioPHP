<p style="margin-bottom: 1em">
    <br>
    <b>Before the Service Project</b>
    <br> 1) Decide whether you want to chair a project at least one week before the day of the project. (Exception: There is no chair for the project and you decide to step up and chair. Do this at least 3 days before the project).
    <br> 2) Click on the "Make Me Chair" option on the event page from the calendar.
    <br> 3) Immediately email the Service VP that you're chairing the project and request information on the directions, contact information of the coordinator, and any other special instructions (e.g. dress code).
    <br> 4) <b>THREE days</b> before the project, send out an email to all those who have signed up by clicking the "Email Everyone Signed Up" button. In this email remind them of the date of the project, location of the project, meeting time and any other special instructions. Make sure you have enough drivers; if not, send out an general APO Email (apo@ocf.berkeley.edu) requesting assistance from drivers.
    <br>
    <b>IMPORTANT:</b> <font color="red">Please don't drop people if it is past the cutoff date</font>. They need to find people that will replace them in that case.
    <br> 5) <b>ONE day</b> before the project, make reminder calls to all those who are going.
    <br>
    <br>
    <b>The Day of the Service Project</b>
    <br> 6) Arrive at least 10 minutes before the scheduled meeting time at the designated location (usually the Underhill parking lot) and make sure everyone is present. If someone is not there yet, call them to confirm that they are on their way.
    <br> 7) <b>15 Minute Rule:</b> If the person doesn't make it in 15 minutes after the designed meeting time, that individual will be left behind and considered a flake.
    <br> 8) <b>Make sure everyone wears their pins.</b> Then collect money from the drivers. Go to the project, do good work. Be mindful of your language! Distribute driver money at the end of the service project. <font color="red">$2 for Berkeley, $3 for East Bay, $4 for San Francisco, $5 for anywhere else</font>. If you cross any toll bridge, ask for a receipt and you'll get reimbursed. <a href="reimbursement.php">How Reimbursements Work</a>.
    <br> 9) Everyone must sing the toast song. If someone needs to leave early, that individual will sing alone or with the rest of the group if possible.
    <br>
    <br>
    <b>After the Service Project</b>
    <br> 10) Fill out the Service Evaluation Form on the APO webpage (go to the event page then click the "Evaluate Event button). Assign hours accordingly, then designate drivers and flakes. Submit the evaluation within one week of the project.
