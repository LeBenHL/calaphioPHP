<strong>Bone Marrow Drive</strong>
<p style="margin-bottom: 1em">
    Of the National Registry for bone marrow donors, only 25% are minorities, and only 6.6% of that 25% are Asians. Unfortunately, all those of minority descent have unique tissue types that are found only in their own respective community, and the odds of finding a match for Asians are one in 10,000 to one in 1,000,000. Brother Jenny Lee (DW) initiated a joint effort with the Asian American Donor Program to recruit potential bone marrow donors on the UC Berkeley campus that will help to save the lives of patients with leukemia, lymphomas, and other life-threatening blood disorders.
</p>

