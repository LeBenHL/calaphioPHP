	<p style="margin: 1.5em 0px;"><strong>Why Finance VP?</strong>
	<br />I understand that many of you were probably surprised to see that I made the decision to run for Finance VP rather than another ExComm position. Finance has always been something that I&#39;ve enjoyed doing, regardless of if it is for personal or work-related reasons. Every event our chapter has runs off of a predetermined budget and as Finance VP, I&#39;m willing to be flexible and receptive to actives&#39; opinions when determining the budget for events, but simultaneously, I also have to be strict and look at things realistically when planning the budget. While these traits may contradict each other, a good Finance VP will know when they must be strict and when they have to be flexible.</p>
	<p style="margin: 1.5em 0px;"><strong>Focus</strong><br />
	As Finance VP, there are two things that I want to focus on; they relate to communication within the chapter, as well as our relationships with fundraising opportunities outside of the chapter. These things are transparency and professionalism. 

It is important that you as actives, understand where our fundraising money is going towards, which is why I want to emphasize that the budget is available on the chapter website for actives to view. We vote as a chapter to approve our budget at our first chapter meeting every semester, and when asked, I want to be as detailed as possible when describing why certain items in our budget were allotted more money than others. If anyone has questions on our budget throughout the semester/year, I am willing to answer them and be as open and thorough as possible. 

Alpha Phi Omega is a professional service fraternity, and it is important that our fundraising sponsors know that as well. We only started working with Cal Dining one year ago, and our past finance vice presidents have done an excellent job in creating and maintaining a stable, positive relationship with them. I hope to continue to strengthen our bonds with Cal Dining so they understand that we are reliable and trustworthy and we are able to maintain any stand they are willing to assign us to. 
</p>
 <p style="margin: 1.5em 0px;"><strong>Qualifications</strong>
<br />In the professional community, I&#39;ve worked with the financial department in EECS for almost a year. Within our chapter, I have attended over 25 fundraisers since I pledged. While I have not dealt with our finances directly (as a Finecomm trainer or trainee), I have tried to watch our budget closely (both the pledging program&#39;s and the chapter&#39;s budgets) and made suggestions, as necessary. I believe in updating documents and entering important paperwork (such as reimbursements) in a timely fashion so you have a clear picture of where our chapter stands and you also receive your reimbursements as soon as possible.

I believe that I am qualified and motivated for this position and if elected, will remain visible within our chapter so you can ask me questions if needed. Thank you for your time and consideration.

