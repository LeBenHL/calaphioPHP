Katie pledged Alpha Phi Omega at Kappa Chapter at Carnegie Mellon University in the Fall of 2003 (RH semester). She decided to pledge after having spent the previous 3 summers as a counselor at a Boy Scout camp. Having seen the impact that scouting could have on the lives of young men, she was seeking to continue her involvement with the Boy Scouts in college. Alpha Phi Omega offered both involvement with scouting and service opportunities.<br><br>

During her time at Kappa Chapter, Katie served the chapter as Service VP, Membership VP, Pledgemaster, President, and Sectionals Convention Chair. Her involvement in scouting continued as a merit badge counselor for Kappa's annual Boy Scout Merit Badge University (MBU) and a counselor for their Girl Scout Scouting College and Interest Project (SCIP) day. She eventually took over as the chair of MBU for two years. Katie also had the opportunity to be involved in APO's involvement in scouting at the National Convention in Louisville where she served as chair of the Scouting and Youth Services Committee. She was also elected as the Kappa Chapter Alumni Association President, an office she held for 2 years.<br><br>

Katie was awarded the Leadership in Service award from Kappa Chapter in 2005 and the Kappa Chapter Distinguished Service Key in May 2008.After graduation from Carnegie Mellon, Katie became a brother at Gamma Gamma. During her time at Gamma Gamma, Katie became a big, started Dinner for the Poor, chaired GG sewing, was a voting delegate at 2010 Nationals, and became a LEADS presenter.
